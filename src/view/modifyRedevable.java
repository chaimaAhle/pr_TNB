    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        RedevableService redServ=new RedevableService();
        Redevable t=getParam();
       int res= redServ.modifier(t);
       if(res==-2){
           JOptionPane.showMessageDialog(null,"Le cin n'est pas valide","ERROR", JOptionPane.ERROR_MESSAGE);  
       }else if(res==-3){
              JOptionPane.showMessageDialog(null,"Le nom n'est pas valide","ERROR", JOptionPane.ERROR_MESSAGE);
       }else if(res==-4){
              JOptionPane.showMessageDialog(null,"Le prenom n'est pas valide","ERROR", JOptionPane.ERROR_MESSAGE);
           }
    else if(res==-5){
          JOptionPane.showMessageDialog(null,"Le numero de Telephone n'est pas valide","ERROR", JOptionPane.ERROR_MESSAGE);
    } else if(res==-6){
          JOptionPane.showMessageDialog(null,"L' adresse n'est pas valide","ERROR", JOptionPane.ERROR_MESSAGE);
    }else if(res==-7){
          JOptionPane.showMessageDialog(null,"Le sexe n'as pas selectioner","ERROR", JOptionPane.ERROR_MESSAGE);
    }
else{
      redevableHelper.update(t);
       JOptionPane.showMessageDialog(null,"modification avec succes","INFO", JOptionPane.INFORMATION_MESSAGE);
           uploadHistory(t);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

