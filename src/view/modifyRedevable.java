private void setParam(Redevable redevable){
 
        jLabel1.setText(redevable.getCin());
        jTextField2.setText(redevable.getNom());
        jTextField3.setText(redevable.getAdresse());
         jTextField5.setText(redevable.getPrenom());
            jTextField4.setText(redevable.getCodePost());
               jTextField6.setText(redevable.getNumTel());
        if (redevable.getSexe().equals("F")){
            jRadioButton2.setSelected(true);
        }else if(redevable.getSexe().equals("M")){
             jRadioButton1.setSelected(true);
        }
}
 private Redevable getParam() {
        Redevable redevable = new Redevable();
        redevable.setCin(jLabel1.getText());
        redevable.setNom(jTextField2.getText());
        redevable.setPrenom(jTextField5.getText());
        if (jRadioButton1.isSelected()) {
            redevable.setSexe("M");
        } else if (jRadioButton2.isSelected()) {
            redevable.setSexe("F");
        }
        redevable.setAdresse(jTextField3.getText());
        redevable.setNumTel(jTextField6.getText());
        redevable.setCodePost(jTextField4.getText());
        return redevable;
    }
